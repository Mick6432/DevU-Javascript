// Data Types

let x = 7;               //number (no difference between an int or a float)
console.log(typeof x);

let xx = 3.14;
console.log(typeof xx);   //number (no difference between an int or a float)

x = 'Harry';             //string
console.log(typeof x);

let y = true;            //boolean
console.log(typeof y);

let a;                   //undefined
console.log(typeof a);

function myFunction() {  //function
    console.log('This is myFunction');
}
console.log(typeof myFunction);

let myArray = [1, 2, 3, 4]; //object
console.log(typeof myArray);