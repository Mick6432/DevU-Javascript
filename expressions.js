//javascript expressions

//Types of expressions
//============================================
// 1. Variable Declaration.
let a;

// 2. Assign a value.
a = 4;

// 3. Perform an evaluation that returns a single value.
let x = 3;
let y = 2;
// Three expressions in on statement:
//    1. Variable Declaration - let z
//    2. Evaluation - x + y
//    3. Assignment - z = x + y
let z = x + y;